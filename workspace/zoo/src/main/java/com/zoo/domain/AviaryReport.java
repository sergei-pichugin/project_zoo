package com.zoo.domain;

public class AviaryReport {

	private Long firstAnimal;
	private Long secondAnimal;
	private int frequency;
	
	public Long getFirstAnimal() {
		return firstAnimal;
	}
	public void setFirstAnimal(Long firstAnimal) {
		this.firstAnimal = firstAnimal;
	}
	public Long getSecondAnimal() {
		return secondAnimal;
	}
	public void setSecondAnimal(Long secondAnimal) {
		this.secondAnimal = secondAnimal;
	}
	public int getFrequency() {
		return frequency;
	}
	public void setFrequency(int frequency) {
		this.frequency = frequency;
	}
	
}
